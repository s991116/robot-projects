#ifndef PINSETUP_H
#define PINSETUP_H

#define PIN_ANALOG_BATTERY_VOLTAGE (A7)
#define PIN_MPU6050_INT (2)
#define PIN_DISTANCE_ECHO (3)
#define PIN_DISTANCE_TRIGGER (8)
#define PIN_SERVO_1 (5)
#define PIN_SERVO_2 (6)
#define PIN_STEPPERMOTOR_SLEEP (12)
#define PIN_POWERLED (10)

//PORTD Pin 0-7
//PORTB Pin 8-13 (12-13 are not usable)
#define PIN_STEPPERMOTOR_LEFT_STEP (7)
#define PIN_STEPPERMOTOR_LEFT_DIR (4)
#define STEPPERMOTOR_LEFT_REVERSE (PORTD &= 0b11101111)
#define STEPPERMOTOR_LEFT_FORWARD (PORTD |= 0b00010000)
#define STEPPERMOTOR_LEFT_HIGH    (PORTD |= 0b10000000)
#define STEPPERMOTOR_LEFT_LOW     (PORTD &= 0b01111111)

#define PIN_STEPPERMOTOR_RIGHT_STEP (11)
#define PIN_STEPPERMOTOR_RIGHT_DIR (9)
#define STEPPERMOTOR_RIGHT_REVERSE (PORTB |= 0b00000010)
#define STEPPERMOTOR_RIGHT_FORWARD (PORTB &= 0b11111101)
#define STEPPERMOTOR_RIGHT_HIGH    (PORTB |= 0b00001000)
#define STEPPERMOTOR_RIGHT_LOW     (PORTB &= 0b11110111)

#endif